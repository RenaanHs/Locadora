public class Series {
    String titulo;
    int classInficativa;
    String categoria;
    String status;
    int temporadas;
    float valor;

    public Series(String titulo, int classInficativa, String categoria, String status, int temporadas, float valor) {
        this.titulo = titulo;
        this.classInficativa = classInficativa;
        this.categoria = categoria;
        this.status = status;
        this.temporadas = temporadas;
        this.valor = valor;
    }
}
