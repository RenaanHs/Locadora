public class Filme {

    String titulo;
    int classInficativa;
    String categoria;
    String status;
    float valor;

    public Filme(String titulo, int classInficativa, String categoria, String status, float valor) {
        this.titulo = titulo;
        this.classInficativa = classInficativa;
        this.categoria = categoria;
        this.status = status;
        this.valor = valor;
    }
}
